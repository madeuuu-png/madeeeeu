export function inicializarCedulas(): void {
  db.execSync(`
    CREATE TABLE IF NOT EXISTS cedulas (
      numDocumento TEXT PRIMARY KEY
    );
  `);

  const { total } = db.getFirstSync<{ total: number }>(
    `SELECT COUNT(*) as total FROM cedulas`
  ) ?? { total: 0 };

  if (total === 0) {
    for (const ced of CEDULAS_INICIALES) {
      db.runSync(`INSERT OR IGNORE INTO cedulas (numDocumento) VALUES (?)`, [ced]);
    }
    console.log(`✅ ${CEDULAS_INICIALES.length} cédulas cargadas en SQLite`);
  }
}

export function existeCedulaLocal(numDocumento: string): boolean {
  const row = db.getFirstSync<{ numDocumento: string }>(
    `SELECT numDocumento FROM cedulas WHERE numDocumento = ?`,
    [numDocumento]
  );
  return row !== null;
}

export function agregarCedulaLocal(numDocumento: string): void {
  db.runSync(`INSERT OR IGNORE INTO cedulas (numDocumento) VALUES (?)`, [numDocumento]);
  console.log(`✅ Cédula agregada local: ${numDocumento}`);
}
